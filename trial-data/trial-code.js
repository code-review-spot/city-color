function processImage(data){
	var pngPath = "./trial-images/"+data.group+"/"+data.id+".png";
	gd.openPng(
		pngPath,
		function(png, path) {
			if(png) {

				var R = 0.0000;
				var G = 0.0000;
				var B = 0.0000;
				var C = 0.0000;

				for(var x=0; x<306; x++){

					var Ra = 0.0000;
					var Ga = 0.0000;
					var Ba = 0.0000;
					var Ca = 0.0000;

					for(var y=0; y<306; y++){

						var pixel = png.getPixel(x,y);
						Ra+= (pixel >>> 16) & 0xFF;
						Ga+= (pixel >>> 8) & 0xFF;
						Ba+= pixel & 0xFF;
						Ca++;

					}

					R+=Ra/Ca;
					G+=Ga/Ca;
					B+=Ba/Ca;
					C = C+1.0000;

				}


				data.colors = {};
				data.colors.R = R/C;
				data.colors.G = G/C;
				data.colors.B = B/C;
				addRow(data);
				
			}
		}
	);
}

function addRow(data){
	var csvPath = "./data.csv";
	var w = csv.createCsvFileWriter(csvPath, {'flags': 'a'});

	var row = []
	row[0]=data.id;
	row[1]=data.group;
	row[2]=data.colors.R;
	row[3]=data.colors.G;
	row[4]=data.colors.B;

	w.writeRecord(row);
}
